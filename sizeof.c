//to find the size of data types using Sizeof function

#include<stdio.h>

void main(){
  int i;
  float f;
  char s;
  double d;
  printf("The size of i type is: %d\n",sizeof(i));
  printf("The size of float type is: %d\n",sizeof(f));
  printf("The size of char type is: %d\n",sizeof(s));
  printf("The size of double type is: %d\n",sizeof(d));
}
